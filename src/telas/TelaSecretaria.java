    private void btnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInfoActionPerformed

        int posicao = cbPacientes.getSelectedIndex();
        if (posicao == 0){
            JOptionPane.showMessageDialog(this, "Selecione um paciente!");
        }else{
            ObjPaciente pac = listaDePacientes.get(posicao);
            
            String sexo = "";
            if (pac.getSexo() == 1){
                sexo = "Masculino";
            }else{
                sexo = "Feminino";
            }
            
            String data = pac.getNascimento().getDate()+"/"+(pac.getNascimento().getMonth()+1)+"/"+pac.getNascimento().getYear();
            JOptionPane.showMessageDialog(this, "Nome: "+pac.getNome()+"\nSexo: "+sexo+"\nNascimento: "+data+"\nTelefone: "+pac.getTelefone()+"\nEndereço: "+pac.getEndereco()
           +"\nRG: "+pac.getRg()+"\nEstado Civil: "+pac.getEstado_civil()+"\nPlano de Saúde: "+pac.getPlano_saude());
        }
       
        
        
    }//GEN-LAST:event_btnInfoActionPerformed

