    public void carregarPacientes (){
        listaDePacientes = PacienteDAO.getPaciente();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        
        ObjPaciente paciente = new ObjPaciente("Selecione...");
        listaDePacientes.add(0, paciente);
        
        for( ObjPaciente pac : listaDePacientes){
            modelo.addElement( pac );
        }
        cbPacientes.setModel( modelo );
        
    }
    public void carregarMedicos (){
        
        listaDeMedicos = FuncionarioDAO.getMedicos();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        
        ObjFuncionario medico = new ObjFuncionario("Selecione...");
        listaDeMedicos.add(0, medico);
        
        for (ObjFuncionario med : listaDeMedicos){
            modelo.addElement(med);
        }
        cbMedicos.setModel(modelo);
        
    }

