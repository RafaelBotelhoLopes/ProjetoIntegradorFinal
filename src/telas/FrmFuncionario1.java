    private void btnSalvarFunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarFunActionPerformed
        
    
        
        ObjFuncionario funcionario = new ObjFuncionario();
        
        
        
        funcionario.setNome(txtNomeFun.getText());
        funcionario.setSexo(cbSexoFun.getSelectedIndex());
        String sData = txtNascimentoFun.getText();
        int dia = Integer.valueOf(  sData.substring(0, 2)   );
        int mes = Integer.valueOf(  sData.substring(3, 5)   );
        int ano = Integer.valueOf(  sData.substring(6, 10)   );
        Date data = new Date(ano, (mes - 1), dia);
        funcionario.setNascimento(data);
        funcionario.setCpf(txtCPFFun.getText());
        funcionario.setRg(txtRGFun.getText());
        funcionario.setTelefone(txtTelefoneFun.getText());
        funcionario.setEstado_civil(cbEstadoCivilFun.getSelectedItem().toString());
        funcionario.setEscolaridade(cbEscolaridadeFun.getSelectedItem().toString());
        funcionario.setEndereco(txtEnderecoFun.getText());
        funcionario.setNacionalidade(txtNacionalidadeFun.getText());
        funcionario.setCargo( (ObjCargo)  cbCargoFun.getSelectedItem() );
        
        FuncionarioDAO.inserir(funcionario);
        limpar();
        telaADM.carregarFuncionarios();
        
    }//GEN-LAST:event_btnSalvarFunActionPerformed

