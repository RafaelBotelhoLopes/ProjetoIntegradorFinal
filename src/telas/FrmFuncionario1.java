    public void carregarCargos (){
        listaDeCargos = CargoDAO.getCargos();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        
        ObjCargo cargo = new ObjCargo("Selecione...");
        listaDeCargos.add(0, cargo);
        
        for (ObjCargo car : listaDeCargos){
            modelo.addElement(car);
        }
        cbCargoFun.setModel(modelo);
    }
    

